package com.kotenkov.basics_of_software_code_development.additional_class;

public class Hole {
    private int a;
    private int b;

    public Hole (int a, int b){
        this.a = b;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
