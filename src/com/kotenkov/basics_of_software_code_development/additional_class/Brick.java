package com.kotenkov.basics_of_software_code_development.additional_class;

public class Brick {
    private int x;
    private int y;
    private int z;

    public Brick (int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
